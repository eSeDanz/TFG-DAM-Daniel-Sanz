    private void jButtonAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAccederActionPerformed
        if (jTextFieldUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debes introducir un usuario.", "Acceso al Sistema", JOptionPane.WARNING_MESSAGE);
        } else if (jPasswordField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debes introducir una clave de acceso.", "Acceso al Sistema", JOptionPane.WARNING_MESSAGE);
        } else {
            usuario = jTextFieldUsuario.getText();
            clave = jPasswordField.getText();
            if (!UC.existe(usuario, clave)){
                JOptionPane.showMessageDialog(this, "Los datos de acceso son incorrectos.", "Acceso al Sistema", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema. Usuario: "+usuario+".", "Matefesa Gesti√≥n", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                VentanaPrincipal venPri = new VentanaPrincipal();
                venPri.setExtendedState(JFrame.MAXIMIZED_BOTH);
                venPri.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButtonAccederActionPerformed

